# calcule a area de um quadrado e mostrar o dobro da área

comprimento = int(input('digite o comprimento: '))
largura = int(input('digite a largura: '))
print(f'o comprimento {comprimento}  a {largura} é igual a área de {comprimento*largura}')
print(f'o dobro da área equivale a : {comprimento*largura*2}')
